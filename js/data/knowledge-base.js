/**
 * تشخیص نیت کاربر
 */
export function detectIntent(text) {
    if (!text) return null;
    const norm = text.toLowerCase().trim();

    // اول intent های اصلی
    for (const intent of KNOWLEDGE_BASE.intents) {
        for (const pattern of intent.patterns) {
            if (norm.includes(pattern.toLowerCase())) {
                return intent;
            }
        }
    }

    // بعد quickCalc
    for (const calc of KNOWLEDGE_BASE.quickCalc) {
        for (const pattern of calc.patterns) {
            if (norm.includes(pattern.toLowerCase())) {
                return calc;
            }
        }
    }

    return null;
}

/**
 * محاسبه سریع kW از توضیحات
 */
export function parseQuickRequest(text) {
    if (!text) return null;
    const norm = text.toLowerCase();

    // تشخیص عدد + کیلووات
    const kwMatch = norm.match(/([۰-۹\d]+(?:\.[۰-۹\d]+)?)\s*(?:کیلو|k|kilo|kw)/);
    if (kwMatch) {
        const numStr = kwMatch[1].replace(/[۰-۹]/g, d => '۰۱۲۳۴۵۶۷۸۹'.indexOf(d));
        return { type: 'kw', value: parseFloat(numStr) };
    }

    // تشخیص تعداد اتاق
    const roomMatch = norm.match(/([۰-۹\d]+)\s*(?:اتاق|اتاقه|خواب|room)/);
    if (roomMatch) {
        const numStr = roomMatch[1].replace(/[۰-۹]/g, d => '۰۱۲۳۴۵۶۷۸۹'.indexOf(d));
        return { type: 'rooms', value: parseInt(numStr) };
    }

    // تشخیص وات
    const wattMatch = norm.match(/([۰-۹\d]+)\s*(?:وات|watt|w)/);
    if (wattMatch) {
        const numStr = wattMatch[1].replace(/[۰-۹]/g, d => '۰۱۲۳۴۵۶۷۸۹'.indexOf(d));
        return { type: 'watt', value: parseInt(numStr) };
    }

    return null;
}

/**
 * پاسخ به سوالات محاسباتی
 */
export function handleCalculation(text) {
    const parsed = parseQuickRequest(text);
    if (!parsed) return null;

    if (parsed.type === 'kw') {
        const kw = parsed.value;
        const panels = Math.ceil((kw * 1000) / 550);
        const invKw = Math.ceil(kw * 1.2);
        const loadWh = kw * 1000 * 4; // 4 ساعت بکاپ
        const batteryAh = Math.ceil((loadWh) / (48 * 0.8));

        return `📊 **محاسبه سریع برای ${kw} kW:**

| تجهیز | تعداد/مقدار |
|-------|-----------|
| ☀️ پنل ۵۵۰W مونو | ${panels} عدد |
| ⚡ اینورتر هیبرید | ${invKw} kW |
| 🔋 باتری لیتیوم ۴۸V | ${batteryAh} Ah (${Math.ceil(batteryAh/200)} باتری ۲۰۰Ah) |
| 🔌 کابل DC | ${panels * 4} متر |
| 🔌 کابل AC | ۲۰ متر |

💰 **قیمت تقریبی:** ${(kw * 700).toLocaleString('fa-IR')} - ${(kw * 1000).toLocaleString('fa-IR')} دلار

✅ برای جزئیات بیشتر به صفحه **BOM** بروید.`;
    }

    if (parsed.type === 'watt') {
        const w = parsed.value;
        const kw = w / 1000;
        const panels = Math.ceil(kw * 1000 / 550);
        const invKw = Math.ceil(kw * 1.2);
        return `📊 **برای بار ${w} W (${kw.toFixed(2)} kW):**

| تجهیز | مقدار |
|-------|-------|
| ☀️ پنل ۵۵۰W مونو | ${panels} عدد |
| ⚡ اینورتر | ${invKw} kW |

اگر ۴ ساعت در روز روشن باشد:
- مصرف روزانه: ${Math.round(w * 4 / 1000)} kWh
- نیاز به باتری: ${Math.ceil((w*4)/(48*0.8))} Ah

💡 صفحه **BOM** برای لیست کامل تجهیزات.`;
    }

    if (parsed.type === 'rooms') {
        const rooms = parsed.value;
        let kw = 2;
        if (rooms >= 4) kw = 5;
        else if (rooms >= 3) kw = 3;
        else if (rooms >= 2) kw = 2.5;
        const panels = Math.ceil((kw * 1000) / 550);

        return `🏠 **برای خانه ${rooms} اتاقه:**

⚡ توان پیشنهادی: **${kw} kW**
☀️ تعداد پنل: **${panels} عدد** (۵۵۰W)
⚡ اینورتر: **${Math.ceil(kw * 1.2)} kW**
🔋 باتری: **${Math.ceil((kw*1000*4)/(48*0.8))} Ah**

این محاسبه تقریبی است. برای محاسبه دقیق، لطفاً لیست وسایل برقی را بفرستید.`;
    }

    return null;
}
